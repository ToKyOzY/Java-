package day03_scanner;

public class C14_Variables {
    public static void main(String[] args) {
        int yas = 36;
        String isim = "zeynep";
        char sira = '1';
        boolean ogrenciMi = false;
        System.out.println(yas); //36
        System.out.println(isim); //zeynep
        System.out.println(sira); //1
        System.out.println(ogrenciMi); //false
        System.out.println("sayi"); //sayi
        System.out.println("yaş:" + yas); //yaş:36
        System.out.println("isim:" + isim); //isim: zeynep
    }
}
